            <h3 class="borderTop">Bushing Selection</h3>
            <p>Bushings are urethane cushions that provide both the suspension 
                and turning resistance for your trucks. Bushings come in a wide 
                variety of shapes and durometers that can dramatically affect how
                your trucks perform. There is no right or wrong way to customize
                your bushings – ultimately it depends on your personal preference,
                weight, and style of riding. Check out our recommendations below
                if you are starting your journey towards the perfect set up.</p>
            <h4>Bushing Shapes</h4>
            <img alt='' src="https://d3ol6qpodzlgj8.cloudfront.net/cdn/farfuture/eh7SlJdnfkPFHvAmzu-22W8c5WPrD9KD7F-TdMBaLog/mtime:1395700358/sites/default/files/content/bushing-guide/bushing-shapes.jpg" />
            <p>Bushings can be divided into four key categories; Cones, Barrels, Stepped Cones (Barricles), and Double Stepped (Eliminators).</p>
            <ul>
                <li><strong>Cone:</strong> Conical shaped bushings offer limited resistance, making them ideal for carving.</li>
                <li><strong>Barrel:</strong> Cylindrical in shape, barrel bushings offer great stability and good resistance for carving, freeriding, and downhill.</li>
                <li><strong>Stepped Cone (Freeride, Barricle):</strong> Designed for freeriding the stepped cone offers more stability than a barrel but is less restrictive than an eliminator.</li>
                <li><strong>Double Stepped (Eliminator):</strong> These bushings limit the trucks range of motion but are great if you are looking for stability. This bushing is the most stable and most restrictive of all four shapes. Great for both freeride and downhill set ups.</li>
            </ul>
            <h4>Rider Weight</h4>
            <img alt='' src="https://d3ol6qpodzlgj8.cloudfront.net/cdn/farfuture/d9JuyMEhCK06V20T4DYHZ2PSsYYXdks0RGPY9HEB2rc/mtime:1395703237/sites/default/files/content/bushing-guide/atlas-weight-guide.jpg" />
            <p class="borderBottom">Bushing durometer designates the hardness of the urethane used in
                the bushing. Durometers can range from 60a all the way to 100a,
                the larger the number the harder the bushing. Harder bushings are
                more restrictive and can be more stable at speed. Softer bushings 
                offer less resistance making them better for turn ability but less
                stable at speed. Keep in mind that stability and turn ability are
                affected proportionally by both the bushings shape and durometer.
                We can provide basic guidelines for choosing the right combination
                but experimentation is the best way to figure out what’s right for you.</p>
