            <p>On the subject of head shapes on mounting hardware, I have preferences that I would like to explain here.  My two favorite types are either Truss heads or Socket Caps.  With the truss style, there is a wider area where the pressure is distributed underneath the head which greatly reduces damage to the deck.  I prefer socket caps because the allen-wrench socket is much deeper and more difficult to damage than a Phillips-head.  My Phillips heads get stripped when they get slammed against the pavement, but the socket caps survive.  You will want to use washers underneath the heads unless it's a drop-through.</p>



